const KEY = "f84fc31d";

const average = (arr) =>
  arr.reduce((acc, cur, i, arr) => acc + cur / arr.length, 0);

//structural component
export default function App() {
  const [movies, setMovies] = useState([]);
  const [watched, setWatched] = useState([]);
  const [isLoading, setIsLoading] = useState(false);
  const [error, setError] = useState("");
  const [query, setQuery] = useState("");
  const [selectedId, setSelectedId] = useState(null);

  /*  
  useEffect(() => console.log("After Initial Render"), []);
  useEffect(() => console.log("After Every Render"));
  console.log("During Render");
  useEffect(() => console.log("After query state change"), [query]); */

  function handleSelectMovie(id) {
    setSelectedId((selectedId) => (id === selectedId ? null : id));
  }

  function handleCloseMovie() {
    setSelectedId(null);
  }

  function handleAddWatched(movie) {
    setWatched((watched) => [...watched, movie]);
  }

  function handleDeleteWatched(id) {
    setWatched((watched) => watched.filter((movie) => movie.imbdID !== id));
  }

  useEffect(
    function () {
      const controller = new AbortController();

      async function fetchMovies() {
        const URL = `http://www.omdbapi.com/?apikey=${KEY}&s=${query}`;
        try {
          setIsLoading(true);
          setError("");
          const res = await fetch(URL, { signal: controller.signal });

          if (!res.ok)
            //in case 400 or other error
            throw new Error("Something went wrong with fetching the movies");

          const data = await res.json();
          // incase of bad search input
          if (data.Response === "False") throw new Error("Movie not found");

          setMovies(data.Search);
          setError("");
        } catch (err) {
          console.log(err.message);
          if (err.name === "TypeError" && err.message === "Failed to fetch") {
            //handles internet disconnect
            setError("Network error: Please check your internet connection.");
          }
          if (err.name !== "AbortError") {
            setError(err.message); // Handles custom error from the 'if (!res.ok)' check
          }
        } finally {
          setIsLoading(false);
        }
      }

      if (!query.length) {
        setMovies([]);
        setError("");
        return;
      }

      handleCloseMovie();
      fetchMovies();
      //cleanup function to abort all previous api calls when a new one is being initiated
      return function () {
        controller.abort();
      };
    },
    [query]
  );

  return (
    <>
      {/* Component composition */}
      <NavBar>
        <Search query={query} setQuery={setQuery} />
        <NumResults movies={movies} />
      </NavBar>
      <Main>
        <Box>
          {/* {isLoading ? <Loader /> : <MovieList movies={movies} />} */}
          {isLoading && <Loader />}
          {!isLoading && !error && (
            <MovieList movies={movies} onSelectMovie={handleSelectMovie} />
          )}
          {error && <ErrorMessage message={error} />}
        </Box>
        <Box>
          {selectedId ? (
            <MovieDetails
              selectedId={selectedId}
              onCloseMovie={handleCloseMovie}
              onAddWatched={handleAddWatched}
              watched={watched}
            />
          ) : (
            <>
              <WatchedSummary watched={watched} />
              <WatchedMovieList
                watched={watched}
                onDeleteWatched={handleDeleteWatched}
              />
            </>
          )}
        </Box>
        {/* option2:passing elements as props. option 1 is better 
        <Box2
          element={
            <>
              <WatchedSummary watched={watched} />
              <WatchedMovieList watched={watched} />
            </>
          }
        /> */}
      </Main>
    </>
  );
}

